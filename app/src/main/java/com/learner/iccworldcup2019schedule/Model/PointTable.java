package com.learner.iccworldcup2019schedule.Model;

public class PointTable {

    private String position;

    private String team;

    private String matchs;

    private String won;

    private String lost;

    private String tied;

    private String noResult;

    private String points;

    private String netRunRate;

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public String getMatchs() {
        return matchs;
    }

    public void setMatchs(String matchs) {
        this.matchs = matchs;
    }

    public String getWon() {
        return won;
    }

    public void setWon(String won) {
        this.won = won;
    }

    public String getLost() {
        return lost;
    }

    public void setLost(String lost) {
        this.lost = lost;
    }

    public String getTied() {
        return tied;
    }

    public void setTied(String tied) {
        this.tied = tied;
    }

    public String getNoResult() {
        return noResult;
    }

    public void setNoResult(String noResult) {
        this.noResult = noResult;
    }

    public String getPoints() {
        return points;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public String getNetRunRate() {
        return netRunRate;
    }

    public void setNetRunRate(String netRunRate) {
        this.netRunRate = netRunRate;
    }

}
